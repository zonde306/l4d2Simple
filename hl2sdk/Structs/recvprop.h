#pragma once

#include <cstdio>
#include <cstdint>

struct RecvProp;

struct RecvTable
{
	RecvProp* m_pProps;
	int m_nProps;
	void* m_pDecoder;
	char* m_pNetTableName;
	bool m_bInitialized;
	bool m_bInMainList;
};

struct RecvProp
{
	char* m_pVarName;
	int m_RecvType;
	int m_Flags;
	int m_StringBufferSize;
	bool m_bInsideArray;
	const void* m_pExtraData;
	RecvProp* m_pArrayProp;
	void* m_ArrayLengthProxy;
	void* m_ProxyFn;
	void* m_DataTableProxyFn;
	RecvTable* m_pDataTable;
	int m_Offset;
	int m_ElementStride;
	int m_nElements;
	const char* m_pParentArrayPropName;
};

typedef enum
{
	DPT_Int = 0,
	DPT_Float,
	DPT_Vector,
	DPT_VectorXY,
	DPT_String,
	DPT_Array,
	DPT_DataTable,
#if 0
	DPT_Quaternion,
#endif
	DPT_Int64,
	DPT_NUMSendPropTypes
} SendPropType;

class DVariant
{
public:
	DVariant();
	DVariant(float val);

	const char* ToString();

	union
	{
		float m_Float;
		long m_Int;
		char* m_pString;
		void* m_pData;
#if 0
		float m_Vector[4];
#else
		float m_Vector[3];
#endif
		int64_t	m_Int64;
	};

	SendPropType m_Type;
};

class CRecvProxyData
{
public:
	const RecvProp* m_pRecvProp; // The property it's receiving.
	DVariant m_Value; // The value given to you to store.
	int m_iElement; // Which array element you're getting.
	int m_ObjectID; // The object being referred to.
};